<h1>Creative Breaking Inspection Rules</h1>
<p>Read the following sentence to the competitors:</p>
<div class="caution">You must stage the performance so that flying boards do not endanger officials, spectators, or other competitors.</div>
<h4>Inspection Checklist</h4>
<ul>
	<li>Boards must be purchased from the event vendor</li>
	<li>Boards must be the correct size for the division
		<ul>
			<li class="agegroup ages-6-9">6-9 years old: &frac14; &times; 10 &times; 12-inch pine board</li>
			<li class="agegroup ages-10-14">10-14 years old: &frac12; &times; 10 &times; 12-inch pine board</li>
			<li class="agegroup ages-15-up">15+ years old: 1 &times; 10 &times; 12-inch pine board</li>
		</ul>
	</li>
	<li>No props such as chairs, trampolines, tables, etc. for aerial techniques.</li>
	<li>Board holders may be used as "human launch pads".</li>
	<li>No glitter, confetti, explosives, or sound makers may be used.</li>
	<li>Blindfolds are permitted.</li>
	<li>No spacers between boards, including fingers.</li>
	<li>Boards may be taped together for ease of holding in multiple board breaks.</li>
	<li>No bricks, cinder blocks, or tiles allowed in the ring.</li>
	<li>Mechanical holders must not have sharp edges and must be approved by ring officials for safety.</li>
</ul>
